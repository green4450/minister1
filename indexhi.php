                        <p>Subsequently, on the formation of a separate Uttarakhand state, he was elected the first Finance, Revenue and Tax Minister of the state in 2014. He was elected as MP from Haridwar region for the 16th Lok Sabha. </p>
                        <p>He is currently the Union Human Resource Development Minister in the Government of India and represents the Haridwar Parliamentary Constituency of Uttarakhand in the Lok Sabha.</p>
                        <h2 class="sabtHdng">A timeline of political journey</h2>
                        <ul>
                          <li>In 1978 he struggled to form a separate state for Uttarakhand.</li>
                          <li>He was elected to the Legislative Assembly of Uttar Pradesh for three consecutive terms from the Karnprayag region in 1991, 1993, and 1996.</li>
                          <li>For the first time in 1997, he became the Cabinet Minister in the Government of Uttar Pradesh (Uttaranchal Development Department).</li>
                          <li>He became the Minister of Culture and Religion of Uttar Pradesh in 1998.</li>
                          <li>In 2000, he was the cabinet minister of 12 departments including Finance, Rural Development, Medicine in the Government of Uttarakhand.</li>
                          <li>He was the Cabinet Minister of Medical, Health, Family Welfare, AYUSH, Science and Technology, and Language Department, Government of Uttarakhand from March 2007 to June 2009.</li>
                          <li>He was the Chief Minister of Uttarakhand from 2009 to 2011.</li>
                          <li>He then served as the National Vice President of Bharatiya Janata Party from 2011 to 2013.</li>
                          <li>He was also elected MLA from Doiwala Legislative Assembly constituency in 2012.</li>
                          <li>He was elected MP from Haridwar Lok Sabha constituency in the year 2014.</li>
                          <li>He was the Chairman of the Government Assurance Committee of the Lok Sabha from the year 2014 to 2019.</li>
