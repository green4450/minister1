                        <h2 class="sabtHdng">The breakthroughs while he was the Chief Minister</h2>
                        <p>Dr. Ramesh Pokhriyal was the fifth Chief Minister of Uttarakhand from 2009 to 2011. He solved complex and sensitive issues such as the inclusion of Haridwar and Udham Singh Nagar in the state of Uttarakhand with the help of political skills, knowledge and sound coordination skills during his Chief Ministership. </p>
                        <p>The International Forum made countless successful efforts to bring Himalayan culture under his leadership. He also reduced the central sales tax from 4% to 1% to encourage small scale industries to operate in the state. 364 depots for all essential commodities of the state were opened under his term that increased revenue from 61.75 crores to 128 crores. Due to his efforts, the total tax collection increased from Rs. 575 crores to Rs. 1100 crores.</p>
                        <p>With the help of science and technology, he initiated many schemes to raise the standard of living of people living in mountainous areas. He launched the Sparsh Ganga campaign to clean the river Ganga and make it pollution-free.</p>
