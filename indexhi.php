                       <h2 class="sabtHdng">Beginning of Sparsh Ganga Campaign</h2>
                       <p>‘Nishank’ started the Sparsh Ganga campaign to clean the river Ganga and make it pollution-free. At the 2010 Kumbh Mela in Haridwar, then Chief Minister Ramesh Pokhriyal ‘Nishank’ said that the state government is committed to maintaining the cleanliness of the Ganga. To make Ganga a world heritage, the Council of Ministers has passed a resolution and sent it to the Central Government. He said, “All of us will have to follow the resolve taken under Sparsh Ganga campaign, only then Ganga can be made clean.”</p>
                       <p>To promote Ganga cleanliness, he announced an incentive of Rs. 11 lakh and to make the event historic, he announced December 17 as Ganga Cleanup Day.</p>
                       <h2 class="sabtHdng">Beginning of Atal Khadyan Yojana</h2>
                       <p>Nishank started the 'Atal Khadyan Yojana' for Below Poverty Line (BPL) and Above Poverty Line (APL) families in the state, which benefited 30 lakh poor people of Uttarakhand. Under this scheme, BPL and APL families can buy wheat and rice at minimum prices of Rs 2 and 3 for 4 and 6 per kg respectively.</p>
                       <h2 class="sabtHdng">Beginning of 108 Emergency Service </h2>
                       <p>The 108 ambulance service under the GVK EMRI started by ‘Nishank’ in Uttarakhand from May 2008, has provided emergency assistance to 11 lakh people so far. With this, 28300 people have been given life in the state due to the 108 ambulance service. The GVK EMRI’s 108 Emergency Service has also set a new record by providing assistance to victims in over 11 lakh emergency cases.</p>
                       <p>Today, 108 is synonymous with best-in-class emergency service and is acknowledged as the most efficient, prompt, reliable and caring service provider in its category.</p>
                       
