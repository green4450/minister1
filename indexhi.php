                       <p>In reality, Vision 2020 was designed as a blueprint to make Uttarakhand the Model State of India. The development of this vision started with the preparation of a roadmap to deal with those challenges in a time-bound manner after brainstorming all the problems before the state, be it the problem of unemployment, the problem of migration, the question of connecting rural areas by road, worrying economy, emergency healthcare, lack of education and university, the question of women empowerment, industrial sector or be it the development or environmental protection. In Vision 2020, he aims to make Uttarakhand a well-informed and developed state and efforts have been made to ensure the active contribution of all in creating an economically sound state. </p>
                       <p>Under Vision 2020 of Chief Minister Ramesh Pokhriyal 'Nishank', work on far-reaching projects were initiated. A committee was formed under the chairmanship of the Chief Secretary. It has been targeted that work is done for the growth rate above the national growth rate with due attention to the deteriorated economic condition of Uttarakhand. The main goal was to prevent the problem of migration by creating new employment opportunities by increasing economic activity. It was emphasized in the vision that efforts should be made to bring qualitative improvement in their lives by bringing basic facilities like education, medicine, drinking water, roads, electricity to the common people. The efficient management of natural resources in view of the sensitivity of Uttarakhand from the environmental point of view includes their conservation enhancement.</p>
